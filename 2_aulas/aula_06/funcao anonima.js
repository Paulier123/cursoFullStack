/* Arrow function (=>)*/
let n1 = 2
let n2 = 4
const somar = (n1,n2) => n1+n2;
    
const subtrair = (n1,n2) => n1-n2;
 
const dividir = (n1,n2) => n1/n2;
  
const multiplicar = (n1,n2) => n1*n2;


console.log(`A soma de ${n1} e ${n2} é ${somar(n1,n2)}`)
console.log(`A subtração de ${n1} e ${n2} é ${subtrair(n1,n2)}`);
console.log(`A divisão de ${n1} e ${n2} é ${dividir(n1,n2)}`);
console.log(`A Multiplicação de ${n1} e ${n2} é ${multiplicar(n1,n2)}`)


